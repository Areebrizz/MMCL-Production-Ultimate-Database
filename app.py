def generate_daily_summary_text(records, metrics, department_filter, start_date, end_date):
    df = pd.DataFrame(records)

    lines = []
    lines.append("📊 MMCL Production Summary")
    lines.append(f"Period: {start_date} to {end_date}")
    lines.append(f"Department: {department_filter}")
    lines.append("")

    if 'department' in df.columns:
        dept_summary = df.groupby('department').agg(
            Plan=('production_plan', 'sum'),
            Actual=('production_actual', 'sum')
        ).reset_index()
        for _, row in dept_summary.iterrows():
            lines.append(f"{row['department']}:")
            lines.append(f"  Plan: {int(row['Plan'])}")
            lines.append(f"  Actual: {int(row['Actual'])}")
            lines.append("")

    lines.append(f"OEE: {metrics['oee']['oee']:.1f}%")
    lines.append(f"  Availability: {metrics['oee']['availability']:.1f}%")
    lines.append(f"  Performance: {metrics['oee']['performance']:.1f}%")
    lines.append(f"  Quality: {metrics['oee']['quality']:.1f}%")
    lines.append("")

    lines.append(f"Total Downtime: {metrics.get('total_downtime', 0):.1f} hrs")

    loss_by_cat = metrics.get('loss_by_category', {})
    if loss_by_cat:
        top_issue = max(loss_by_cat.items(), key=lambda x: x[1])
        lines.append(f"Top Issue: {top_issue[0]} ({top_issue[1]:.1f} hrs)")

    lines.append("")
    lines.append(f"Units Per Man: {metrics.get('units_per_man', 0):.2f}")
    lines.append(f"Scrap Rate: {metrics.get('scrap_rate', 0):.1f}%")
    lines.append(f"First Pass Yield: {metrics.get('first_pass_yield', 0):.1f}%")

    return "\n".join(lines)

