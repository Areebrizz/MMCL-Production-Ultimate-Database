def production_line_visualizer():
    update_activity()
    st.header("🚌 Live Production Line Visualizer")

    lines = get_production_lines()
    all_vehicles = get_vehicles_in_production()

    # ---------------- Feature #3: Bottleneck Detector & Feature #6: Heat Map ----------------
    st.markdown("### 🔥 Station Heat Map & Bottleneck Detector")

    in_progress = [v for v in all_vehicles if v['status'] == 'in_progress']

    bottleneck_threshold = st.slider("Bottleneck threshold (vehicles at a station)", 1, 10, 3,
                                      help="If a station has this many or more vehicles, it's flagged as a bottleneck")

    heat_data = []
    overall_max_count = 0
    overall_max_label = None

    for line_name, stations in lines.items():
        line_vehicles = [v for v in in_progress if v['line_name'] == line_name]
        for station in stations:
            count = len([v for v in line_vehicles if v['station_name'] == station])
            heat_data.append({"Line": line_name, "Station": station, "Vehicles": count})
            if count > overall_max_count:
                overall_max_count = count
                overall_max_label = f"{line_name} - {station}"

    heat_df = pd.DataFrame(heat_data)

    if overall_max_count >= bottleneck_threshold and overall_max_label:
        st.error(f"🚨 Bottleneck Detected: **{overall_max_label}** has **{overall_max_count}** vehicles waiting!")
    else:
        st.success("✅ No bottlenecks detected at the current threshold.")

    if not heat_df.empty:
        pivot = heat_df.pivot(index="Line", columns="Station", values="Vehicles").fillna(0)
        fig = go.Figure(data=go.Heatmap(
            z=pivot.values,
            x=pivot.columns,
            y=pivot.index,
            colorscale=[[0, "#2ecc71"], [0.4, "#f1c40f"], [1, "#e74c3c"]],
            text=pivot.values,
            texttemplate="%{text}",
            showscale=True
        ))
        fig.update_layout(title="Vehicles per Station (Green=Clear, Yellow=Watch, Red=Bottleneck)",
                           height=350, template="plotly_white")
        st.plotly_chart(fig, use_container_width=True)

    st.markdown("---")

    # Tab interface for different lines
    tabs = st.tabs(list(lines.keys()))

    for i, (line_name, stations) in enumerate(lines.items()):
        with tabs[i]:
            st.subheader(f"{line_name} Production Flow")

            line_vehicles = [v for v in all_vehicles if v['line_name'] == line_name and v['status'] == 'in_progress']

            cols = st.columns(len(stations))

            for col_idx, (col, station) in enumerate(zip(cols, stations)):
                with col:
                    station_vehicles = [v for v in line_vehicles if v['station_name'] == station]
                    badge = "🔴" if len(station_vehicles) >= bottleneck_threshold else "🟢"
                    st.markdown(f"**{badge} {station}** ({len(station_vehicles)})")
                    st.markdown("---")

