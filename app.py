def get_action_items(status_filter=None):
    try:
        query = supabase.table("action_tracking").select("*").order("created_at", desc=True)
        if status_filter and status_filter != "All":
            query = query.eq("status", status_filter)
        response = query.execute()
        items = response.data
        # Auto-flag overdue
        today = date.today()
        for item in items:
            if item['status'] == "Open" and item.get('due_date'):
                try:
                    due = pd.to_datetime(item['due_date']).date()
                    if due < today:
                        item['status_display'] = "Overdue"
                    else:
                        item['status_display'] = "Open"
                except:
                    item['status_display'] = item['status']
            else:
                item['status_display'] = item['status']
        return items
    except Exception as e:
        st.error(f"Error fetching action items: {str(e)}")
        return []

