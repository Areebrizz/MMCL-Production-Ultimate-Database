def action_tracker_page():
    update_activity()
    st.header("🛠️ Action Tracker")

    st.markdown("Track corrective actions raised against downtime, quality, or other issues. "
                 "Status auto-flags as **Overdue** if past due date and still Open.")

    tab1, tab2 = st.tabs(["📋 Action Items", "➕ New Action Item"])

    with tab1:
        status_filter = st.selectbox("Filter by Status", ["All", "Open", "Closed", "Overdue"])
        items = get_action_items()

        if status_filter != "All":
            items = [i for i in items if i.get('status_display') == status_filter]

        if items:
            for item in items:
                status_disp = item.get('status_display', item['status'])
                badge = {"Open": "🟡", "Closed": "🟢", "Overdue": "🔴"}.get(status_disp, "⚪")

                with st.expander(f"{badge} [{item['issue_type']}] {item['issue_description'][:60]} — {status_disp}"):
                    st.write(f"**Department:** {item.get('department', 'N/A')}")
                    st.write(f"**Assigned To:** {item.get('assigned_to', 'N/A')}")
                    st.write(f"**Due Date:** {item.get('due_date', 'N/A')}")
                    st.write(f"**Created By:** {item.get('created_by', 'N/A')}")
                    st.write(f"**Created At:** {pd.to_datetime(item['created_at']).strftime('%Y-%m-%d %H:%M') if item.get('created_at') else 'N/A'}")
                    st.write(f"**Description:** {item['issue_description']}")

                    if item['status'] != "Closed":
                        if st.button("✅ Mark as Closed", key=f"close_{item['id']}"):
                            update_activity()
                            if update_action_status(item['id'], "Closed"):
                                log_audit_event(st.session_state.user_id, "action_closed", "action_tracking", item['id'], f"Closed action: {item['issue_description'][:50]}")
                                st.success("Action item closed!")
                                time.sleep(1)
                                st.rerun()
                    else:
                        if st.button("↩️ Reopen", key=f"reopen_{item['id']}"):
                            update_activity()
                            if update_action_status(item['id'], "Open"):
                                st.success("Action item reopened!")
                                time.sleep(1)
                                st.rerun()
        else:
            st.success("✅ No action items found for this filter!")

    with tab2:
        with st.form("new_action_form", clear_on_submit=True):
            issue_type = st.selectbox("Issue Type", DOWNTIME_CATEGORIES[1:] + ["Quality Defect", "Other"])
            issue_description = st.text_area("Issue Description")
            assigned_to = st.text_input("Assign To")
            due_date = st.date_input("Due Date", value=date.today() + timedelta(days=1))
            department = st.selectbox("Department", ["Assembly Shop", "Paint Shop", "Weld Shop", "QAHSE", "PDI", "System"])

            if st.form_submit_button("➕ Create Action Item"):
                update_activity()
                if issue_description and assigned_to:
                    result = create_action_item(
                        issue_type=issue_type,
                        issue_description=issue_description,
                        assigned_to=assigned_to,
                        due_date=due_date,
                        department=department,
                        created_by=st.session_state.username
                    )
                    if result:
                        log_audit_event(st.session_state.user_id, "action_created", "action_tracking", result['id'], f"Created action: {issue_description[:50]}")
                        st.success("Action item created!")
                        time.sleep(1)
                        st.rerun()
                    else:
                        st.error("Failed to create action item")
                else:
                    st.error("Please fill in Issue Description and Assigned To")

